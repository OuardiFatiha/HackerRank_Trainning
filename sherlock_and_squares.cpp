int squares(int a, int b) {
    int b_prime = int(sqrt(b));
    int a_prime = int(sqrt(a));
    cout<<"b':"<<b_prime<<endl;
    cout<<"a':"<<a_prime<<endl;
    float res;
    int c = 0;
    for(int i=a_prime; i<=b_prime; i++){
        res = pow(i, 2);
        if((res>=a) && (res<=b)){
            c++;
        }
    }
    return c;
}

