/*
 * Complete the 'nonDivisibleSubset' function below.
 *
 * The function is expected to return an INTEGER.
 * The function accepts following parameters:
 *  1. INTEGER k
 *  2. INTEGER_ARRAY s
 */

int nonDivisibleSubset(int k, vector<int> s) {
    //corner cases
    if (k<2){
        return 1;
    }  
    //counting equal remainders when divided be k, using basic hashing concept  
    int arr[k]={0};
    for (int i=0;i<s.size();i++){
        arr[s[i]%k]++;
    }
    //conting no. of subsets in c
    int c=0;
    //main logic
    for (int i=1;i<k/2;i++){
        c=c+max(arr[i],arr[k-i]);
    }
    //all extra cases
    if(arr[0]>1){
        c++;
    }
    if(k%2==0 && arr[k/2]>0){
        c++;
    }
    if(k%2!=0){
        int l=k/2;
        c=c+max(arr[l],arr[k-l]);
    }
    //returning all the counts
    return c;        
}

